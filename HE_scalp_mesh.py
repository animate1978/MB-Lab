def centroid(co):
    co = array(co, float)
    return sum(co, axis=0)/len(co)

def surface_constraint(ob, target):
    ob.constraints.new(type='SHRINKWRAP')
    con = ob.constraints["Shrinkwrap"]
    con.name = f"CON_{ob.name}"
    con.target = target
    con.use_track_normal = True
    con.track_axis = 'TRACK_Z'

