def hair_update():
    bpy.ops.particle.disconnect_hair(all=True)
    bpy.ops.particle.connect_hair(all=True)

def new_hair(ob, Name, count, length, step, curve_guide=1):
    ob.modifiers.new(Name, 'PARTICLE_SYSTEM')
    psys = bpy.data.particles[Name]
    psys.type = 'HAIR'
    psys.count = count
